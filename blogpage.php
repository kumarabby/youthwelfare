      <p>Keralotsavam, a rare and unique youth festival, is conducted mainly for the youth who have no proper platform to present their cultural and physical talents. In this festival youth in the age group of 15-35 can participate in Cultural, Arts and Sports competitions in block, district level to State level. There are some selected cultural items, which are not popular in mainstream cultural scene, has also been included in this festival.
Keralotsavam is now a widely accepted festival for the Youth of Kerala. As a result every youth in this State is getting a chance to project him/her self to the fore-front in every sphere of life.Kerala created a model for the nation by initiating a colourful youth festival “Keralotsavam”, which is conducted with gaiety and fanfare every year.
